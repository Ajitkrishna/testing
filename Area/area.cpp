#include "pch.h"
#include "area.h"


float Area::Areaofrect(float& a, float& b)
{
    return a * b;
}
float Area::Areaofcrc(float& a)
{
    return 3.14 * a * a;
}

float Area::Areaofsqr(float& a)
{
    return a * a;
}
float Area::Areaoftrl(float& a, float& b)
{
    return (0.5 * a * b);
}


